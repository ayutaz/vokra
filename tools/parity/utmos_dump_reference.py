#!/usr/bin/env python3
"""UTMOS22 reference dump — **imports the real upstream implementation** (M5-15 T18).

# Why this script is written the way it is

Kokoro taught the lesson the hard way (`92dbc92`): a reference generated by a
*mirror* of the model validates nothing. A self-consistent mirror agrees with
the Rust port by construction, so parity goes green while the audio is wrong.
This dumper therefore refuses to define any UTMOS layer itself. It:

1. downloads the upstream ``sarulab-speech/UTMOS-demo`` sources
   (``model.py`` + ``lightning_module.py``) at a pinned revision and
   **sha256-verifies** them,
2. imports those files as-is and lets *them* build the network — including
   ``fairseq.checkpoint_utils.load_model_ensemble_and_task``, i.e. the real
   fairseq ``Wav2Vec2Model`` at the pinned commit,
3. loads the real UTMOS22-strong checkpoint into that module tree,
4. registers forward hooks on the upstream modules and records the tensors
   flowing between them.

If any of those steps fails it **aborts loudly**. Nothing is stubbed,
approximated, or reconstructed (NFR-QL-04 fabricated-pass ban).

# Environment (M5-15 T38/T39)

The upstream pin is torch 1.11.0, which has **no macOS-arm64 wheel**, so the
band this dumper measures includes a deliberate torch-version delta. The
measured, working combination on the M1 is:

    Python 3.9 + torch 2.8.0 + fairseq @ d03f4e77 + pytorch-lightning 1.9.5

Python 3.11 does **not** work (fairseq@2022's dataclass defaults trip 3.11's
tightened check) and torch 2.2.2's headers do not compile against the macOS 26
SDK. See ``docs/adr/M5-15-utmos.md`` §(d) and the probe log at
``~/.cache/vokra-eval/out/utmos-flip/env-probe*.log``.

# Usage

::

    ~/.cache/vokra-eval/venv-utmos-e/bin/python tools/parity/utmos_dump_reference.py \\
        --ckpt   ~/.cache/vokra-eval/out/utmos-probe/ckpt/'epoch=3-step=7459.ckpt' \\
        --w2v    ~/.cache/vokra-eval/out/utmos-probe/ckpt/wav2vec_small.pt \\
        --clip   tests/parity/utmos/ref-clip.wav \\
        --outdir ~/.cache/vokra-eval/out/utmos-flip/reference

Outputs (per tap): a raw little-endian f32 ``.bin`` plus one ``manifest.json``
carrying every tap's shape, the final score, the repeat-run band, and the
exact library versions used.
"""

from __future__ import annotations

import argparse
import hashlib
import json
import os
import struct
import sys
import tempfile
import urllib.request
import wave

# Pinned upstream revision of the HF space (recorded in the campaign-2 probe:
# ~/.cache/vokra-eval/out/utmos-probe/decision-memo.md §(i)).
SPACE_REV = "47212055c2ec"
SPACE_FILES = ("model.py", "lightning_module.py")
SPACE_URL = "https://huggingface.co/spaces/sarulab-speech/UTMOS-demo/resolve/{rev}/{name}"
# sha256 of the two upstream sources at SPACE_REV, pinned so a silently
# changed upstream file cannot slip into a reference dump.
SPACE_SHA256 = {
    "model.py": "79ca330b7d7fd83304b7254e10c5c3a0e0c30c2b8ae3af90847353ae8f6336d4",
    "lightning_module.py": "def23a75821bb662ba95220bbb241f6762d59689f763bc007d9b8c3b254c313f",
}

# Upstream inference constants (score.py).
DOMAIN_ID = 0
JUDGE_ID = 288


def die(msg: str):
    print(f"utmos_dump_reference: {msg}", file=sys.stderr)
    raise SystemExit(2)


def fetch_upstream(dst: str, record_hashes: bool) -> "dict[str, str]":
    """Downloads + verifies the upstream sources into `dst`."""
    got = {}
    for name in SPACE_FILES:
        url = SPACE_URL.format(rev=SPACE_REV, name=name)
        try:
            with urllib.request.urlopen(url, timeout=60) as r:
                blob = r.read()
        except Exception as e:  # noqa: BLE001 - the message matters, not the type
            die(f"could not fetch upstream {name} from {url}: {e}")
        h = hashlib.sha256(blob).hexdigest()
        want = SPACE_SHA256.get(name)
        if want is None:
            if not record_hashes:
                print(
                    f"NOTE: no pinned sha256 for {name}; got {h} "
                    f"(re-run with --record-hashes to print the pin block)",
                    file=sys.stderr,
                )
        elif h != want:
            die(
                f"upstream {name} sha256 {h} != pinned {want} — the reference "
                f"source changed; re-pin deliberately, never silently"
            )
        got[name] = h
        with open(os.path.join(dst, name), "wb") as f:
            f.write(blob)
    return got


def read_wav_mono16k(path: str) -> "list[float]":
    with wave.open(path, "rb") as w:
        if w.getnchannels() != 1:
            die(f"{path} has {w.getnchannels()} channels; the reference clip must be mono")
        sr = w.getframerate()
        if sr != 16000:
            die(f"{path} is {sr} Hz; UTMOS requires 16 kHz (no silent resample)")
        sw = w.getsampwidth()
        frames = w.readframes(w.getnframes())
    if sw == 2:
        n = len(frames) // 2
        ints = struct.unpack(f"<{n}h", frames)
        return [v / 32768.0 for v in ints]
    if sw == 4:
        n = len(frames) // 4
        return list(struct.unpack(f"<{n}f", frames))
    die(f"{path} has {sw}-byte samples; expected PCM16 or float32")


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--ckpt", required=True)
    ap.add_argument("--w2v", required=True, help="wav2vec_small.pt (architecture source)")
    ap.add_argument("--clip", required=True, help="mono 16 kHz WAV")
    ap.add_argument("--outdir", required=True)
    ap.add_argument("--repeats", type=int, default=3, help="re-runs for the band")
    ap.add_argument("--record-hashes", action="store_true")
    args = ap.parse_args()

    import torch

    os.makedirs(args.outdir, exist_ok=True)
    work = tempfile.mkdtemp(prefix="utmos-upstream-")
    hashes = fetch_upstream(work, args.record_hashes)
    if args.record_hashes:
        print("SPACE_SHA256 = {")
        for k, v in hashes.items():
            print(f'    "{k}": "{v}",')
        print("}")

    # Upstream's load_ssl_model() hard-codes cp_path='wav2vec_small.pt'
    # relative to CWD, so run from a dir where that name resolves.
    link = os.path.join(work, "wav2vec_small.pt")
    if not os.path.exists(link):
        os.symlink(os.path.abspath(args.w2v), link)
    sys.path.insert(0, work)
    os.chdir(work)

    try:
        import fairseq  # noqa: F401
        import pytorch_lightning as pl  # noqa: F401
        import lightning_module  # the UPSTREAM file, just downloaded
    except Exception as e:  # noqa: BLE001
        die(
            f"could not import the upstream stack ({type(e).__name__}: {e}). "
            f"This dumper must import the real implementation; writing a "
            f"local re-implementation instead is banned (the reference would "
            f"validate nothing). See docs/adr/M5-15-utmos.md §(d) for the "
            f"environment that works."
        )

    print(f"torch {torch.__version__}  fairseq {fairseq.__version__}  pl {pl.__version__}")

    # torch >= 2.6 defaults `torch.load(weights_only=True)`, which rejects the
    # omegaconf objects the 2022 checkpoint stores in its Lightning
    # hyper-parameters. Allowlisting those classes is torch's own sanctioned
    # escape hatch and changes no model code — it is part of the recorded
    # env delta (the pinned torch 1.11 predates the flag entirely).
    # (The checkpoint is the one this repo's own probe downloaded and
    # sha256-verified — `44c57e3e…`, recorded in the campaign-2 decision memo
    # — so restoring the pinned torch's behaviour is not a trust downgrade.)
    _torch_load = torch.load

    def _load_as_torch_1_11(*a, **kw):
        kw.setdefault("weights_only", False)
        return _torch_load(*a, **kw)

    torch.load = _load_as_torch_1_11
    try:
        model = lightning_module.BaselineLightningModule.load_from_checkpoint(
            args.ckpt, map_location="cpu"
        ).eval()
    finally:
        torch.load = _torch_load

    # ---- taps on the UPSTREAM modules -------------------------------------
    ssl = model.feature_extractors[0].ssl_model
    taps: "dict[str, torch.Tensor]" = {}

    def grab(name):
        def hook(_mod, _inp, out):
            t = out[0] if isinstance(out, tuple) else out
            if isinstance(t, torch.Tensor):
                taps[name] = t.detach().clone()

        return hook

    handles = [
        ssl.feature_extractor.register_forward_hook(grab("conv_out")),
        ssl.layer_norm.register_forward_hook(grab("feature_ln")),
        ssl.post_extract_proj.register_forward_hook(grab("feat_proj")),
        ssl.encoder.pos_conv.register_forward_hook(grab("pos_conv")),
        ssl.encoder.layer_norm.register_forward_hook(grab("enc_in_ln")),
        model.output_layers[0].decoder_rnn.register_forward_hook(grab("blstm_out")),
        model.output_layers[1].net.register_forward_hook(grab("head_out")),
    ]
    for i, layer in enumerate(ssl.encoder.layers):
        handles.append(layer.register_forward_hook(grab(f"enc_block_{i}")))

    audio = read_wav_mono16k(args.clip)
    wav = torch.tensor(audio, dtype=torch.float32).unsqueeze(0).unsqueeze(0)
    batch = {
        "wav": wav,
        "domains": torch.zeros(1, dtype=torch.int),
        "judge_id": torch.ones(1, dtype=torch.int) * JUDGE_ID,
    }

    scores = []
    for _ in range(max(1, args.repeats)):
        with torch.no_grad():
            out = model(batch)
        # score.py: output.mean(dim=1).squeeze(1) * 2 + 3
        scores.append(float(out.mean(dim=1).squeeze(1).numpy()[0] * 2 + 3))
    for h in handles:
        h.remove()

    band = max(scores) - min(scores)
    print(f"scores over {len(scores)} run(s): {scores}  band {band:.3e}")

    # The SSL feature the conditioner consumes = last encoder block output.
    manifest = {
        "clip": os.path.basename(args.clip),
        "sample_rate": 16000,
        "samples": len(audio),
        "score": scores[0],
        "scores_all_runs": scores,
        "rerun_band": band,
        "domain_id": DOMAIN_ID,
        "judge_id": JUDGE_ID,
        "upstream": {
            "space": "sarulab-speech/UTMOS-demo",
            "space_rev": SPACE_REV,
            "sha256": hashes,
            "fairseq": fairseq.__version__,
            "torch": torch.__version__,
            "pytorch_lightning": pl.__version__,
            "python": sys.version.split()[0],
        },
        "taps": {},
    }
    for name, t in sorted(taps.items()):
        arr = t.reshape(-1).to(torch.float32).numpy()
        path = os.path.join(args.outdir, f"{name}.bin")
        with open(path, "wb") as f:
            f.write(arr.tobytes())
        manifest["taps"][name] = {
            "shape": list(t.shape),
            "numel": int(arr.size),
            "file": f"{name}.bin",
            "sha256": hashlib.sha256(arr.tobytes()).hexdigest(),
        }
        print(f"tap {name:16s} {list(t.shape)}")

    # The converter's weight-norm fold, computed by torch itself, so the Rust
    # fold is checked against the reference rather than against its own math.
    pc = ssl.encoder.pos_conv[0]
    folded = torch._weight_norm(pc.weight_v, pc.weight_g, 2).detach()
    fp = os.path.join(args.outdir, "pos_conv_folded_weight.bin")
    with open(fp, "wb") as f:
        f.write(folded.reshape(-1).to(torch.float32).numpy().tobytes())
    manifest["pos_conv_folded_weight"] = {
        "shape": list(folded.shape),
        "file": "pos_conv_folded_weight.bin",
    }

    with open(os.path.join(args.outdir, "manifest.json"), "w", encoding="utf-8") as f:
        json.dump(manifest, f, indent=2, sort_keys=True)
        f.write("\n")
    print(f"wrote {args.outdir}/manifest.json")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
