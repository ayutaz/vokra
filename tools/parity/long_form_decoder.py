#!/usr/bin/env python3
"""long_form_decoder.py — M3-01-T12 Metal ⇄ CUDA differential parity fixture.

Purpose
-------
`MetalDecodeSession` (M2-01) and `CudaDecodeSession` (M2-03) are API-symmetric
device-resident decoder-step drivers. They should emit the same logits within
FP32 `atol = 0.01` (NFR-QL-01) for the same input, weights, and initial state.
CI has no runner with both GPUs available at once, so this script is the
per-run *fixture generator*: run it on the Metal CI (M1 iMac) and on the CUDA
CI (self-hosted RTX 4090 once M2-14 stands it up), each writes a JSON summary
containing the per-token logits (checksum + shape + selected token IDs) for
the same synthetic 60 s / 120 s audio input. The owner cross-compares the two
JSON summaries (or the CI does when both runners land) to verify long-form
parity.

Design (M3-01-T12)
------------------

*   **Zero-dep** — the script uses only Python stdlib (``json``, ``argparse``,
    ``subprocess``, ``os``, ``sys``, ``hashlib``, ``pathlib``). No numpy, no
    torch — the fixture is *the runtime's own decoded logits*, so it is
    generated by driving `vokra-cli` (which owns the CUDA/Metal decoder-step
    session) rather than a Python reference. This keeps NFR-DS-02 intact.

*   **Synthetic input** — 60 s / 120 s of PCM (sine + noise, xorshift seed
    matching ``whisper_cuda_large_v3_rtf.rs::make_sample_pcm``) so the fixture
    is reproducible across runs without shipping large audio.

*   **What lands in the JSON**:

    - ``segment_seconds``: 60 / 120
    - ``backend``: 'metal' | 'cuda'
    - ``device_summary``: from ``vokra-cli bench --info`` (name, driver,
      compute cap) so mismatched hardware surfaces at diff time
    - ``token_ids``: greedy-decoded token id list — the tight parity contract
      (Metal and CUDA must emit the same tokens under greedy)
    - ``logits_sha256``: SHA-256 over the flat FP32 logits emitted by the
      first N tokens (checksum only — the raw logits are too large to commit
      as a fixture; the two runners' checksums either match to the bit or the
      diff step surfaces the delta by re-running with a smaller N and dumping
      the raw)

*   **Not a required check** — like the M2-14 vast.ai variance harness, this
    is a *reference* fixture. Automatic gate promotion happens only after
    both runners land and are stable for 4+ consecutive weekly runs, per
    ADR M3-01 §2 (d).

*   **Skip cleanly when the runner cannot run the workload** — a missing
    GGUF path, a broken ``vokra-cli`` binary, or an absent GPU emits a
    ``{"skipped": true, "reason": "..."}`` JSON and returns exit 0. Never a
    fabricated pass (FR-EX-08); the owner CI reads the ``skipped`` field.

Usage::

    ./long_form_decoder.py \
        --backend metal|cuda \
        --gguf whisper-large-v3.gguf \
        --seconds 60 \
        --vokra-cli target/release/vokra-cli \
        --output long-form-{backend}-{seconds}s.json

The two output JSONs (from Metal + CUDA) are diffed by a companion script (or
manual owner inspection) — the M3-01 completion condition only requires the
fixture to *land*; the cross-run comparison is future work (out of scope for
this WP, tracked as M2-14 continuation).

Zero-dep invariant (NFR-DS-02): this script imports only Python stdlib; it
does not touch pip or ``requirements.txt``.
"""

from __future__ import annotations

import argparse
import hashlib
import json
import os
import subprocess
import sys
from pathlib import Path


def make_sample_pcm_hash(seconds: int, sample_rate: int = 16_000) -> str:
    """Deterministic hash of the synthetic PCM used for the fixture.

    Mirrors ``whisper_cuda_large_v3_rtf.rs::make_sample_pcm`` byte-for-byte
    (xorshift64* seed 0x9E37_79B9_7F4A_7C15, 440 Hz sine + noise mix); we hash
    it here without materialising the whole buffer so the JSON stays tiny.
    Both Metal and CUDA runners feed exactly the same synthetic PCM.
    """
    n = seconds * sample_rate
    h = hashlib.sha256()
    x = 0x9E37_79B9_7F4A_7C15
    import struct
    import math

    for i in range(n):
        x ^= (x >> 12) & 0xFFFF_FFFF_FFFF_FFFF
        x ^= (x << 25) & 0xFFFF_FFFF_FFFF_FFFF
        x ^= (x >> 27) & 0xFFFF_FFFF_FFFF_FFFF
        bits = ((x * 0x2545_F491_4F6C_DD1D) >> 40) & 0xFFFF_FFFF
        noise = bits / (1 << 24) * 2.0 - 1.0
        t = i / sample_rate
        tone = math.sin(2.0 * math.pi * 440.0 * t)
        sample = 0.5 * tone + 0.05 * noise
        h.update(struct.pack("<f", sample))
    return h.hexdigest()


def emit_skip(reason: str, output: Path | None) -> None:
    """Write a `{skipped: true, reason: ...}` JSON and exit 0.

    Never a fabricated pass (FR-EX-08): the owner CI reads the ``skipped``
    field to distinguish "measured within bound" from "no measurement".
    """
    payload = {"skipped": True, "reason": reason}
    body = json.dumps(payload, indent=2)
    if output:
        output.write_text(body + "\n")
    print(body)
    sys.exit(0)


def main() -> int:
    parser = argparse.ArgumentParser(
        description="M3-01-T12 long-form decoder fixture generator "
        "(Metal ⇄ CUDA parity)"
    )
    parser.add_argument(
        "--backend",
        choices=["metal", "cuda"],
        required=True,
        help="Which GPU backend to drive on this runner",
    )
    parser.add_argument(
        "--gguf",
        required=True,
        help="Absolute path to whisper-large-v3.gguf on the runner",
    )
    parser.add_argument(
        "--seconds",
        type=int,
        default=60,
        choices=[30, 60, 120],
        help="Synthetic audio length (30/60/120)",
    )
    parser.add_argument(
        "--vokra-cli",
        default="target/release/vokra-cli",
        help="Path to the built vokra-cli binary",
    )
    parser.add_argument(
        "--output",
        type=Path,
        required=True,
        help="Where to write the JSON summary",
    )
    args = parser.parse_args()

    # Preflight: the GGUF must exist and vokra-cli must be a binary. Missing
    # either is a clean skip (fixture cannot be generated — the owner has to
    # stage them on the runner).
    if not os.path.isfile(args.gguf):
        emit_skip(f"GGUF not found: {args.gguf}", args.output)
    if not os.path.isfile(args.vokra_cli) or not os.access(args.vokra_cli, os.X_OK):
        emit_skip(
            f"vokra-cli not executable: {args.vokra_cli}. Build with "
            f"`cargo build --release -p vokra-cli --features {args.backend}`",
            args.output,
        )

    # Deterministic input hash — the two runners must feed identical PCM.
    pcm_hash = make_sample_pcm_hash(args.seconds)

    # M3-01-T12 (structural): this initial land only emits the fixture header
    # + input hash + a placeholder ``token_ids`` / ``logits_sha256`` block.
    # Wiring the vokra-cli invocation that actually decodes the synthetic PCM
    # requires a ``vokra-cli bench --decode-synth --seconds N --backend cuda|metal
    # --emit-tokens`` CLI surface that is not part of the M3-01 completion set —
    # tracked as a follow-up on the M2-14 self-hosted runner branch. On the
    # current land day this script writes a well-formed skip payload so both
    # Metal and CUDA CI legs run green without fabricating a pass.
    emit_skip(
        f"M3-01-T12 fixture generator scaffolding — vokra-cli synth-decode "
        f"surface pending self-hosted runner integration (M2-14 continuation). "
        f"Input hash (SHA-256, {args.seconds}s @ 16 kHz): {pcm_hash}",
        args.output,
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())
